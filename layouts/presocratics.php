                <ul>
                    <li>Liczba jest arche</li>
                    <li>Liczba jest rozumiana jako pewien materialny element</li>
                    <li>Zasadą wyjściową jest Jedynka (punkt), z niej powstaje Dwójka (linia), następnie Trójka
